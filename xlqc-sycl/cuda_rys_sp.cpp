int cuda_ij2intindex(int i, int j)
{
  if (i < j) {
    int t = i; i = j; j = t;
  }
  return i * (i + 1) / 2 + j;
}

int cuda_fact(int n){
  int result = 1;
  for (int i = 2; i <= n; i++) result *= i;
  return result;
}

int cuda_binomial(int a, int b){
  return cuda_fact(a)/(cuda_fact(b)*cuda_fact(a-b));
}

void cuda_Roots(int n, float X, float roots[], float weights[]){
  if (n <= 3)
    cuda_Root123(n,X, roots,weights);
  else if (n==4) 
    cuda_Root4(X, roots,weights);
  else if (n==5)
    cuda_Root5(X, roots,weights);
  else
    cuda_Root6(n,X, roots,weights);
  return;
}

